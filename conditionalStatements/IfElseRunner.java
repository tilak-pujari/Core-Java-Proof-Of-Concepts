package conditionalStatements;

public class IfElseRunner {

	public static void main(String[] args) {
		int i = 29;
		if (i == 23) {
			System.out.println("I is equal to 23");
		} else if (i == 24) {
			System.out.println("I is equal to 24");
		} else if (i == 25) {
			System.out.println("I is equal to 25");
		} else {
			System.out.println("I is neither equal to 23 nor 24 nor 25");
		}

	}

}
